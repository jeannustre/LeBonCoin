import UIKit

/// Static colors used within the app. Available in both dark and light mode.
class NamedColors {
    /// The background color.
    static let leBonBackground = UIColor(named: "bon-background")
    /// The color for elements (cells)
    static let leBonElement = UIColor(named: "bon-element")
    /// A color that constrasts strongly with backgrounds and elements.
    static let leBonConstrast = UIColor(named: "bon-contrast")
    /// An accent color.
    static let leBonAccent = UIColor(named: "bon-accent")
    /// The color for the "Urgent" indicator.
    static let leBonUrgent = UIColor(named: "bon-urgent")
}
